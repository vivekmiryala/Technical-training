  set = [3, 34, 4, 12, 5, 2]
  sum1 = 9
  sum2 = 30

  if is_subset_sum(set, sum1):
    print("Subset with sum", sum1, "exists.")
  else:
    print("Subset with sum", sum1, "does not exist.")

  if is_subset_sum(set, sum2):
    print("Subset with sum", sum2, "exists.")
  else:
    print("Subset with sum", sum2, "does not exist.")